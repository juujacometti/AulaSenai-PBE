# Você é um assistente de loja e precisa calcular o dobro do preço de três produtos para uma promoção relâmpago: • Produto A: R$ 5,00 / • Produto B: R$ 8,00 / • Produto C: R$ 12,00

def dobro(dobro):
    preco = dobro * 2
    print(f"O dobro do preço do produto é:\nR${preco:.2f}")

dobro(5.00)
dobro(8.00)
dobro(12.00)
